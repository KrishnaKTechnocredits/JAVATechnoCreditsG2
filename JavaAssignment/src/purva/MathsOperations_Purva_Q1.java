package purva;

public class MathsOperations_Purva_Q1 {

	float sum(int n1, int n2) {
		return (n1 + n2);
	}

	float multi(int n1, int n2) {
		return (n1 * n2);
	}

	float sub(int n1, int n2) {
		return (n1 - n2);
	}

	float divide(int n1, int n2) {
		return (n1 / n2);
	}

	int remainder(int n1, int n2) {
		return (n1 % n2);
	}

}
