package technoCredits.abstractionDemo;

// Interfaces are used to create prototype in java.
// I is a keyword in java.
// All the methods in I are by default public & abstract method.
// All the variables in I are by default public, final and static.
// I extends another I.
// Class implements another interface.
// I cannot be instantiated.(Can not create a object of interface.) 

interface A {
	 
   public abstract void m1();
   abstract void m2();
   void m3();
	void m4();
	void m5();
	void m6();
   
   static int i = 10;
   public int j = 20;
   

}